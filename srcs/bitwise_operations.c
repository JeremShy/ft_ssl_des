#include <ft_ssl.h>

uint32_t	rotl(uint32_t n, uint32_t x)
{
	return ((x << n) | (x >> (32 - n)));
}

uint32_t	rotr(uint32_t n, uint32_t x)
{
	return (rotl(32 - n, x));
}
